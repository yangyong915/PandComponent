package com.panda.module_message.mvp.model;

import android.app.Application;

import com.google.gson.Gson;
import com.jess.arms.integration.IRepositoryManager;
import com.jess.arms.mvp.BaseModel;

import com.jess.arms.di.scope.ActivityScope;

import javax.inject.Inject;

import com.panda.module_message.mvp.contract.MessageListContract;


@ActivityScope
public class MessageListModel extends BaseModel implements MessageListContract.Model {
    private Gson mGson;
    private Application mApplication;

    @Inject
    public MessageListModel(IRepositoryManager repositoryManager, Gson gson, Application application) {
        super(repositoryManager);
        this.mGson = gson;
        this.mApplication = application;
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mGson = null;
        this.mApplication = null;
    }

}